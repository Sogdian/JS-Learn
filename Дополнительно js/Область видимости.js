var food = 'котлеты с пюрешкой';

var eatDinner = function () {
  console.log('Поел ' + food);
};

eatDinner();// Выведет: Поел котлеты с пюрешкой

//переопределение глобальной переменной
//С этого места в коде и ниже, старого значения переменной больше нет.
//Поэтому, когда мы вызвали функцию после переопределения переменной, функция использовала новое значение.
food = 'сельдерей';

eatDinner();// Выведет: Поел сельдерей



